public class OperatorsSample {
    public static void main(String[] args) {
        int num1 = 12;
        int num2 = 2;
        System.out.println(num1+num2);
        System.out.println(num1-num2);
        System.out.println(num1/num2);
        boolean status = num2 < num1;//false
        boolean status1 = num2 == num1;
        System.out.println(status);
        System.out.println(status1);
    }
}
